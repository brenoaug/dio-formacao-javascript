let verificandoNumero;
const numeroVerificado = 0;


verificandoNumero = numeroVerificado % 2 === 0;

// == igualdade
// === igualdade estrita
// != diferente
// !== diferente estrito

if(numeroVerificado === 0){
  console.log(`O número ${numeroVerificado} é neutro`);
}else if(verificandoNumero !== true){
    console.log(`O número ${numeroVerificado} é impar`);
}else{
  console.log(`O número ${numeroVerificado} é par`);
}

